import { Schema, model } from "mongoose";

export interface UserDocument {
  email: string;
  passwordHash: string;
  storeName: string;
  createdAt: Date;
}

const userSchema = new Schema<UserDocument>(
  {
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true
    },
    passwordHash: {
      type: String,
      required: true
    },
    storeName: {
      type: String,
      required: true,
      trim: true
    },
    createdAt: {
      type: Date,
      default: Date.now
    }
  },
  {
    versionKey: false
  }
);

export const UserModel = model<UserDocument>("User", userSchema);
